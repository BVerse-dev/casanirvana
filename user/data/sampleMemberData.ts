// Helper function to get sample data by role
export const getSampleMembersByRole = (role: 'admin' | 'management' | 'user'): CommunityMember[] => {
  switch (role) {
    case 'admin':
      return sampleAdmins;
    case 'management':
      return sampleCommittee;
    case 'user':
      return sampleMembers;
    default:
      return [];
  }
};

